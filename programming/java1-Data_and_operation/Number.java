
public class Number {

	public static void main(String[] args) {
		// Operator
		System.out.println(6 + 2);
		System.out.println(6 - 2);
		System.out.println(6 * 2);
		System.out.println(6 / 2);
		
		System.out.println(Math.PI);
		
		// new line
		System.out.println("Hello \nWorld");
		// escape string
		System.out.println("Hello \"World\" ");
	}

}
